def fix_article(stock_symbol, data):
    """修复单篇文章"""
    base_path = Path('/Users/zzw868/PycharmProjects/PythonProject/vercel-money-factory/src/pages/stock-analysis')
    article_dir = base_path / f'{stock_symbol}-20260331'
    md_file = article_dir / 'index.md'
    
    if not md_file.exists():
        print(f"❌ {stock_symbol}: 文件不存在 - {md_file}")
        return False
    
    # 读取当前文件
    with open(md_file, 'r', encoding='utf-8') as f:
        content = f.read()
    
    # 创建新的 Frontmatter
    new_frontmatter = f'''---
title: "{data['title']}"
description: "{data['description']}"
pubDate: 2026-03-31
category: "Stock Analysis"
tags: ["{stock_symbol.upper()}股价分析", "{stock_symbol.upper()}走势预测", "美股投资", "股票分析报告", "即将推出更多深度内容"]
---
'''
    
    # 替换 Frontmatter 并添加内容
    lines = content.split('\n')
    in_frontmatter = False
    frontmatter_end = 0
    
    for i, line in enumerate(lines):
        if line.strip() == '---':
            if not in_frontmatter:
                in_frontmatter = True
            else:
                frontmatter_end = i
                break
    
    # 构建新内容
    new_content = new_frontmatter + '\n\n' + data['content'].strip() + '\n'
    
    # 写入文件
    with open(md_file, 'w', encoding='utf-8') as f:
        f.write(new_content)
    
    print(f"✅ {stock_symbol.upper()}: 修复成功")
    return True

